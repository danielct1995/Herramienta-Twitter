package Tweets;

public class Element {

	private String key;
	private int value;
	private float stats;
	
	
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public float getStats() {
		return stats;
	}
	public void setStats(float stats) {
		this.stats = stats;
	}
	

}
